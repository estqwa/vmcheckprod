export function getLegalLocale(language?: string): LegalLocale {
  return language?.toLowerCase().startsWith('kk') ? 'kk' : 'ru';
}

function applyOperatorDetails(text: string, locale: LegalLocale): string {
  const replacements =
    locale === 'kk'
      ? [
          [
            `Сервис операторы: ${SERVICE_NAME}, ${WEBSITE_URL} мекенжайы бойынша қолжетімді цифрлық сервис.`,
            `Сервис операторы: ${LEGAL_OPERATOR_KK}. Сервис ${SERVICE_NAME} брендімен ${WEBSITE_URL} мекенжайы бойынша қолжетімді.`,
          ],
          [
            `Деректер операторы: ${SERVICE_NAME}, ${WEBSITE_URL} мекенжайы бойынша қолжетімді цифрлық сервис.`,
            `Деректер операторы: ${LEGAL_OPERATOR_KK}. Сервис ${SERVICE_NAME} брендімен ${WEBSITE_URL} мекенжайы бойынша қолжетімді.`,
          ],
          [
            `Ұйымдастырушы: ${SERVICE_NAME}, ${WEBSITE_URL} мекенжайы бойынша қолжетімді цифрлық сервис.`,
            `Ұйымдастырушы: ${LEGAL_OPERATOR_KK}. Сервис ${SERVICE_NAME} брендімен ${WEBSITE_URL} мекенжайы бойынша қолжетімді.`,
          ],
        ]
      : [
          [
            `Оператор сервиса: ${SERVICE_NAME}, цифровой сервис, доступный по адресу ${WEBSITE_URL}.`,
            `Оператор сервиса: ${LEGAL_OPERATOR_RU}. Сервис работает под брендом ${SERVICE_NAME} и доступен по адресу ${WEBSITE_URL}.`,
          ],
          [
            `Оператор данных: ${SERVICE_NAME}, цифровой сервис, доступный по адресу ${WEBSITE_URL}.`,
            `Оператор данных: ${LEGAL_OPERATOR_RU}. Сервис работает под брендом ${SERVICE_NAME} и доступен по адресу ${WEBSITE_URL}.`,
          ],
          [
            `Организатор: ${SERVICE_NAME}, цифровой сервис, доступный по адресу ${WEBSITE_URL}.`,
            `Организатор: ${LEGAL_OPERATOR_RU}. Сервис работает под брендом ${SERVICE_NAME} и доступен по адресу ${WEBSITE_URL}.`,
          ],
        ];

  return replacements.reduce((result, [searchValue, replaceValue]) => result.replace(searchValue, replaceValue), text);
}

export function getLegalCopy(language?: string): LegalDocumentSet {
  const locale = getLegalLocale(language);
  const document = LEGAL_COPY[locale];

  return {
    terms: applyOperatorDetails(document.terms, locale),
    privacy: applyOperatorDetails(document.privacy, locale),
    officialRules: applyOperatorDetails(document.officialRules, locale),
  };
}
